function [A,B,C,D] = bam(a,b,c,d,e,f)
    
    %x1 = (a/(b*s-c))
    %x2 = d/(e*s-f)
    %
    %(b*s-c)*x1 = a(U-x2)
    %-> b*s*x1 = a*U-a*x2+c*x1
    %-> s*x1 =(a/b)*U - (a/b)*x2 +(c/b)*x1
    %
    %(e*s-f)*x2 = d*x1
    %-> e*s*x2 = d*x1+ f*x2
    %-> s*x2 = (d/e)*x1+ (f/e)*x2
    
    
    %Volviendo al tiempo-> 
    %X1 =(a/b)*u - (a/b)*x2 +(c/b)*x1
    %X2 = (d/e)*x1+ (f/e)*x2
    
    A = [c/b,-a/b;d/e,f/e];
    B = [a/b;0];
    C =[1, 0];
    D = 0  
end

